package classes;

public class Tuner {
    public void on(){
        System.out.println("Sintonizador ligado");
    }

    public void off(){
        System.out.println("Sintonizador desligado");
    }

    public void pause(){
        System.out.println("Sintonizador pausado");
    }

    public void start(){
        System.out.println("Sintonizador iniciado");
    }
}
