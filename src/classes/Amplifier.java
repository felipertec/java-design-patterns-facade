package classes;

public class Amplifier {
    public void on(){
        System.out.println("Aplificador Ligado");
    }

    public void off(){
        System.out.println("Aplificador Desligado");
    }

    public void pause(){
        System.out.println("Aplificador Pausado");
    }

    public void start(){
        System.out.println("Aplificador Iniciado");
    }
}
