package classes;

public class DvdPlayer {
    public void on(){
        System.out.println("Dvd Player Ligado");
    }

    public void off(){
        System.out.println("Dvd Player Desligado");
    }

    public void pause(){
        System.out.println("Dvd Player Pausado");
    }

    public void start(){
        System.out.println("Dvd Player iniciado");
    }
}
